package com.philips.dmis.swt.ui.toolkit.widgets;

public class JsRenderException extends RuntimeException {
    public JsRenderException() {
    }

    public JsRenderException(String message) {
        super(message);
    }

    public JsRenderException(String message, Throwable cause) {
        super(message, cause);
    }

    public JsRenderException(Throwable cause) {
        super(cause);
    }
}
